def add(num1, num2):    # This is our function. It takes two arguments: num1 and num2.
    return num1 + num2

def subtract(num1, num2):    # This is our function. It takes two arguments: num1 and num2.
    return num1 - num2

def multiply(num1, num2):    # This is our function. It takes two arguments: num1 and num2.
    return num1 * num2

def divide(num1, num2):    # This is our function. It takes two arguments: num1 and num2.
    return num1 / num2

def exp(num1, num2):    # This is our function. It takes two arguments: num1 and num2.
    return num1 ** num2

print(add(3, 4))         
print(exp(1, 5))         
print(multiply(5, 3))         
print(divide(1, 5))  
print(subtract(1, 5))  